    private DefaultTableModel montarTabela(List<Fcp> listaFcp) {
        String[] colunas = {"ID", "Aliquota", "Base Legal", "Valido até"};
        DefaultTableModel tabela = new DefaultTableModel();
        tabela.setColumnIdentifiers(colunas);

        for (int i = 0; listaFcp.size() > i; i++) {
            Fcp fcp = listaFcp.get(i);

            String[] linha = {
                Integer.toString(fcp.getId()),
                fcp.getAliquota(),
                fcp.getBaseLegal(),
                fcp.getValidoAte()
            };
            tabela.addRow(linha);
        }
        return tabela;
    }

    private String getIdItemSelecionado() {
        int posicao = jtableFcp.getSelectedRow();
        if (posicao == -1) {
            JOptionPane.showMessageDialog(null, "Selecione um item da tabela!");
        }
        return (String) jtableFcp.getValueAt(posicao, 0);
    }

