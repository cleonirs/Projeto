    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        Fcp fcp = new Fcp();
        fcp.setAliquota(txtAliquota.getText());
        fcp.setBaseLegal(txtBaseLegal.getText());
        fcp.setValidoAte(txtValidoAte.getText());

        if (fcpEdicao != null) {
            fcpEdicao.setAliquota(txtAliquota.getText());
            fcpEdicao.setBaseLegal(txtBaseLegal.getText());
            fcpEdicao.setValidoAte(txtValidoAte.getText());
            FcpJPA.atualizar(fcpEdicao);
            JOptionPane.showMessageDialog(null, "FCP atualizada com sucesso!");

        } else {
            FcpJPA.cadastrar(fcp);
            JOptionPane.showMessageDialog(null, "FCP cadastrada com sucesso!");

        }
        jtableFcp.setModel(montarTabela(FcpJPA.listar()));
        jScrollPane1.setViewportView(jtableFcp);

        txtAliquota.setText("");
        txtBaseLegal.setText("");
        txtValidoAte.setText("");

        fcpEdicao = null;
    }//GEN-LAST:event_btnSalvarActionPerformed

